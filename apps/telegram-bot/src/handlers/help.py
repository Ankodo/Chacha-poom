@router.message(Command("help"))
async def cmd_help(message: Message) -> None:
    await message.answer(
        HELP_TEXT,
        reply_markup=_help_platforms_keyboard(),
        parse_mode="HTML",
    )


@router.callback_query(F.data == "help")
async def cb_help(callback: CallbackQuery) -> None:
    await callback.answer()
    if callback.message:
        await callback.message.edit_text(
            HELP_TEXT,
            reply_markup=_help_platforms_keyboard(),
            parse_mode="HTML",
        )


@router.callback_query(F.data.startswith("help:"))
async def cb_help_platform(callback: CallbackQuery) -> None:
    await callback.answer()
    platform = callback.data.split(":", 1)[1]  # type: ignore[union-attr]
    text = PLATFORM_TEXTS.get(platform, HELP_TEXT)

    if callback.message:
        await callback.message.edit_text(
            text,
            reply_markup=_help_back_keyboard(),
            parse_mode="HTML",
        )
